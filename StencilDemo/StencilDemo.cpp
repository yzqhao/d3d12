static Math::Vec4 SphericalToCartesian(float radius, float theta, float phi)
{
	return Math::Vec4(
		radius * sinf(phi) * cosf(theta),
		radius * cosf(phi),
		radius * sinf(phi) * sinf(theta),
		1.0f);
}

